/* 浏览器执行JS简介
浏览器分成两部分 渲染引擎和JS引擎
渲染引擎：用来解析HTML和CSS，俗称内核，比如chrome浏览器的blink，老版本的webkit
JS引擎：也称为JS解释器。用来读取网页中的JS代码，对其处理后运行，比如chrome浏览器的V8 */